"""Road waypoint definitions for OSRM-based geometry resolution.

Each road is defined by its name, city, and a list of waypoints (start, optional
via-points, end) as [lat, lng]. The OSRM routing service will snap these to the
real road network and return a dense polyline that follows actual road geometry.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import Literal


@dataclass
class RoadWaypoints:
    """Defines a road by its name and ordered waypoints for OSRM routing."""
    road_name: str
    city_id: str
    waypoints: list[list[float]]  # [[lat, lng], ...] — start, via, end


@dataclass
class RoadSegment:
    """Resolved road segment with dense coordinates from OSRM."""
    road_name: str
    city_id: str
    coords: list[list[float]]  # [[lat, lng], ...] — dense polyline
    congestion_level: Literal["free", "slow", "heavy"] = "free"


